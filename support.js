function getEmptyCell(array) {
    var empty = new Array();

    for (var i = 0; i < 4; i++) {
        for (var j = 0; j < 4; j++) {
            if (array[i][j] == 0)
                empty.push([i, j]);
        }
    }
    return empty;
}